// Conditionals
let num = 1

// if
if(num == 1){
    console.log(`Num = ${num}!`)
}

// if else
let number = -1
if(number > 0){
    console.log(`${number} is positive!`)
} else {
    console.log(`${number} is negative!`)
}

// if else if else
let num = 0 

if(num > 0) {
    console.log(`${num} is positive!`)
} else if(num < 0) {
    console.log(`${num} is negative!`)
} else {
    console.log(`${num} is zero!`)
}
